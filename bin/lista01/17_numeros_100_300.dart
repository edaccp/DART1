// Objetivo
// Mostrar todos os números de 100 até 300, de 10 em 10.

// Instruções
// Escreva um algoritmo em Dart que imprima os números de 100 até 300, de 10 em 10.
// Casos de Testes
// Saída:
//   100
//   110
//   120
//   130
//   140
//   150
//   160
//   170
//   180
//   190
//   200
//   210
//   220
//   230
//   240
//   250
//   260
//   270
//   280
//   290
//   300

void main(List<String> args) {
  for (var i = 100; i <= 300; i += 10) {
    print(i);
  }
}
